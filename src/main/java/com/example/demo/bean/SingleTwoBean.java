package com.example.demo.bean;

public class SingleTwoBean {
    private int st;
    private int sId;
    private String sIssue;
    private int sSum;

    public int getSt() {
        return st;
    }

    public void setSt(int st) {
        this.st = st;
    }

    public int getsId() {
        return sId;
    }

    public void setsId(int sId) {
        this.sId = sId;
    }

    public String getsIssue() {
        return sIssue;
    }

    public void setsIssue(String sIssue) {
        this.sIssue = sIssue;
    }

    public int getsSum() {
        return sSum;
    }

    public void setsSum(int sSum) {
        this.sSum = sSum;
    }
}
