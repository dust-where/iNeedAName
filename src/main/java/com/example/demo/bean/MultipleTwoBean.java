package com.example.demo.bean;

public class MultipleTwoBean {
    private int mt;
    private int mId;
    private String mIssue;
    private int mSum;

    public int getMt() {
        return mt;
    }

    public void setMt(int mt) {
        this.mt = mt;
    }

    public int getmId() {
        return mId;
    }

    public void setmId(int mId) {
        this.mId = mId;
    }

    public String getmIssue() {
        return mIssue;
    }

    public void setmIssue(String mIssue) {
        this.mIssue = mIssue;
    }

    public int getmSum() {
        return mSum;
    }

    public void setmSum(int mSum) {
        this.mSum = mSum;
    }
}
